public class SimpleCalculation {
    public static void main(String[] args) {
        // Define two numbers
        int number1 = 10;
        int number2 = 20;

        // Calculate the sum of the two numbers
        int sum = number1 + number2;

        // Print the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is: " + sum);
    }
}
